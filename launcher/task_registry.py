def task_map() -> dict[str, dict]:
    return {task["id"]: task for task in TASKS if task.get("runner") != "separator"}


def group_tasks() -> dict[str, list[dict]]:
    groups = {"installation": [], "main": [], "suggested": [], "fixes": [], "servers": [], "settings": [], "docs": [], "tools": [], "db": []}
    for task in TASKS:
        groups.setdefault(task["group"], []).append(task)
    return groups
