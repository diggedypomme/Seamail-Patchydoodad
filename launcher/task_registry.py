def python_task(
    task_id: str,
    label: str,
    group: str,
    script_path: str,
    *,
    args: list[str] | None = None,
    description: str,
    kind: str = "oneshot",
    requires_admin: bool = False,
    available: bool = True,
    open_url: str | None = None,
    launch_mode: str = "background",
    has_interface: bool = False,
    interface_key: str | None = None,
    interface_label: str | None = None,
    interface_mode: str = "generic",
    conflicts_with: list[str] | None = None,
    venv_key: str | None = None,
    venv_label: str | None = None,
    venv_requirements: str | None = None,
    venv_packages: list[str] | None = None,
    can_stop: bool = True,
    stop_label: str | None = None,
    no_start: bool = False,
) -> dict:
    return {
        "id": task_id,
        "label": label,
        "group": group,
        "runner": "python",
        "script_path": script_path,
        "args": args or [],
        "description": description,
        "kind": kind,
        "requires_admin": requires_admin,
        "available": available,
        "open_url": open_url,
        "launch_mode": launch_mode,
        "has_interface": has_interface,
        "interface_key": interface_key or task_id,
        "interface_label": interface_label or label,
        "interface_mode": interface_mode,
        "conflicts_with": conflicts_with or [],
        "venv_key": venv_key,
        "venv_label": venv_label or venv_key,
        "venv_requirements": venv_requirements,
        "venv_packages": venv_packages or [],
        "can_stop": can_stop,
        "stop_label": stop_label or "Stop",
        "no_start": no_start,
    }


